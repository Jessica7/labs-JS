/* 
    Math.pow()
    -retorna o numero Elevado a potência
*/

 var value = Math.pow(2,3)
 console.log(vlaue); 

/*
   Math.sqrt() 
   -calcula a raiz quadrada
*/

var value = Math.sqrt(9)
console.log(value); 

/*
  Math.round() 
  -Arredonda Número(inteiro) mais próximo
*/
var value = Math.round(9.13);
console.log(value); 

/*
  Math.ceil() 
  -arredonda maior ou igual ao argumento  
*/
var value = Math.ceil(9.03);//10
console.log(value); 

/*
  Math.floor() 
  -Ao contrário do ceil, arredonda menor
  ou igual ao seu argumento 
*/
var value = Math.floor(9.99);// 9
console.log(value); 

/*
  Math.max() 
  -encontra o valor máximo
*/
var value = Math.max(2,8,67,4,9);//67
console.log(value); 

/*
  Math.min()
  -encontra o valor mínimo
*/
var value = Math.min(2,8,67,4,9);
console.log(value);

/*
  Math.abs() 
  -irá retornar o valor absoluto
*/
var value = Math.abs(-7);// 7
console.log(value);

/*
  Math.sin() e Math.cos();
  -retorna o seno de um número
*/
var value = Math.sin(30);
console.log(value);

var value = Math.cos(30);
console.log(value);

/* 
  Math.random()
  -sorteia um nº rândomico
*/
var value = Math.random();
console.log(value); 

/*
  Math.PI() 
*/
var value = Math.PI();
console.log(value);